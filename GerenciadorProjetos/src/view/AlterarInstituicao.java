    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) throws ConexaoException, DAOException {//GEN-FIRST:event_btnAlterarActionPerformed
       Instituicao instituicao = new Instituicao();
        if(txtId.getText().isEmpty()){
            txtId.setText("0");
        }
        instituicao.setIdInstituicao(Integer.parseInt(txtId.getText().trim()));
        instituicao.setNome(txtNome.getText().trim());
        instituicao.setLogradouro(txtLogradouro.getText().trim());
        instituicao.setFone1(txtTelefoneCelular.getText().trim());
        instituicao.setFone2(txtTelefoneRecidencial.getText().trim());
        instituicao.setEmail(txtEmail.getText().trim());
        Fachada fachada = Fachada.getInstancia();

        try{
            fachada.alterarInstituicao(instituicao);
            JOptionPane.showMessageDialog(null, "InstituiÃ§Ã£o Atualizado Com Sucesso");
        }catch(AtualizarException ex){
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }catch(CampoException ex){
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
        
    }//GEN-LAST:event_btnAlterarActionPerformed

